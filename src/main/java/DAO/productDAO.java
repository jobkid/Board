package DAO;

import java.sql.*;
import java.util.*;
import DTO.productDTO;

public class productDAO {
	private productDAO() {}
	private static productDAO instance = new productDAO();
	public static productDAO getInstance() {
		return instance;
	}
	
	//커넥션 기능 처리 메서드
	public Connection getConnection() throws Exception{
		Connection conn = null;
		String url = "jdbc:mysql://127.0.0.1:3306/study";
		String id = "root";
		String pw = "iotiot";
		Class.forName("com.mysql.cj.jdbc.Driver");
		conn=DriverManager.getConnection(url, id, pw);
		return conn;
	}
	
	//커넥션 연결해제 처리 메서드
	public static void close(Connection conn, Statement stmt, ResultSet rs) {
		try {//select 해제
			rs.close();
			stmt.close();
			conn.close();
		}catch(Exception e) {
			System.out.println("연결해제 중 오류 발생");
		}
	}
	
	public static void close(Connection conn, Statement stmt) {
		try {//update해제
			stmt.close();
			conn.close();
		}catch(Exception e) {
			System.out.println("연결 해제 중 오류발생 : "+e);
		}
	}
	
	//데이터베이스 조회 기능
	public List<productDTO> selectAllProducts(){
		List<productDTO> list=new ArrayList<productDTO>();
		String sql = "select * from product order by code desc";
		Connection conn = null;
		PreparedStatement pstmt = null;
		ResultSet rs = null;
		try {
			conn = getConnection();
			pstmt = conn.prepareStatement(sql);//rs가 있으므로 여기서 sql해야 함.
			rs = pstmt.executeQuery();
			while(rs.next()) {
				productDTO p = new productDTO();
				p.setCode(rs.getInt("code"));
				p.setName(rs.getString("name"));
				p.setPrice(rs.getInt("price"));
				p.setPictureurl(rs.getString("pictureurl"));
				p.setDescription(rs.getString("description"));
				list.add(p);
			}
		}catch(Exception e) {
			System.out.println("전체조회 중 오류 발생 : "+e);
		}finally {
			productDAO.close(conn, pstmt, rs);
		}
		return list;
	}
}
